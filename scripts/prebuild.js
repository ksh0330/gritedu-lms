function bundleCssFromSources() {
  const cssFiles = collectCssFileList();
  let bundledCSS = '';
  let loadedFiles = 0;
  const missingFiles = [];

  for (const file of cssFiles) {
    const filePath = path.join(ROOT_DIR, file);
    if (!fs.existsSync(filePath)) {
      missingFiles.push(file);
      continue;
    }

    const content = fs.readFileSync(filePath, 'utf8');
    if (content.trim().length > 20 && !content.trim().match(/^\/\*.*TODO.*\*\/\s*$/)) {
      bundledCSS += `${content}\n`;
      loadedFiles += 1;
      const fileSize = Buffer.byteLength(content, 'utf8');
      console.log(`   ✅ ${file} 포함 (${(fileSize / 1024).toFixed(2)}KB)`);
    }
  }

  if (loadedFiles === 0) {
    const mainCssPath = path.join(ASSETS_DIR, 'css', 'main.css');
    if (!fs.existsSync(mainCssPath)) {
      throw new Error('No CSS sources found for bundling.');
    }

    console.log('⚠️  분리된 CSS 파일이 없습니다. main.css @import를 inline합니다.');
    const mainContent = fs.readFileSync(mainCssPath, 'utf8');
    bundledCSS = inlineLocalCssImports(mainContent, path.dirname(mainCssPath));
    loadedFiles = 1;
    const bundledSize = Buffer.byteLength(bundledCSS, 'utf8');
    console.log(`   ✅ main.css @import inline (${(bundledSize / 1024).toFixed(2)}KB)`);
  } else if (missingFiles.length > 0) {
    throw new Error(`Required CSS file(s) missing: ${missingFiles.join(', ')}`);
  }

  bundledCSS = inlineLocalCssImports(bundledCSS, path.join(ASSETS_DIR, 'css'));
  const remainingImports = findLocalCssImports(bundledCSS);
  if (remainingImports.length > 0) {
    throw new Error(
      `Bundled CSS still contains local @import: ${remainingImports.map((item) => item.href).join(', ')}`
    );
  }

  return { bundledCSS, loadedFiles };
}

