// 버전 번호 생성 (타임스탬프 기반)
function generateVersion() {
  const timestamp = Date.now();
  const date = new Date(timestamp);
  const dateStr = date.toISOString().slice(0, 10).replace(/-/g, '');
  const timeStr = date.toTimeString().slice(0, 8).replace(/:/g, '');
  return `${dateStr}-${timeStr}`;
}

function escapeHtmlAttribute(value) {
  return String(value)
    .replace(/&/g, '&amp;')
    .replace(/"/g, '&quot;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;');
}

function injectBuildVersionMeta(content, version) {
  const meta = `<meta name="grit-build-version" content="${escapeHtmlAttribute(version)}" />`;
  content = content.replace(/\s*<meta\s+name=["']grit-build-version["'][^>]*>\s*/i, '\n');
  if (/<head[^>]*>/i.test(content)) {
    return content.replace(/<head([^>]*)>/i, `<head$1>\n  ${meta}`);
  }
  return `${meta}\n${content}`;
}

function removeBundledStylesheets(content) {
  return content.replace(
    /\n?\s*<link\b(?=[^>]*\brel=["']stylesheet["'])(?=[^>]*\bhref=["'][^"']*\/assets\/css\/(?:base|components|pages)\/[^"']*\.css(?:\?[^"']*)?["'])[^>]*>\s*/gi,
    '\n'
  );
}

// HTML 파일에 버전 번호 적용
function applyVersionToHTML(content, version) {
  // 기존 버전 쿼리 파라미터 제거 (중복 방지)
  content = content.replace(/[?&]v=[^"'\s&]*/g, '');
  content = removeBundledStylesheets(content);
  content = content.replace(/\/assets\/main\.png/g, '/assets/main.webp');

  content = injectBuildVersionMeta(content, version);
  
  // HTML만 약한 캐시 방지 (서버 헤더가 이미 no-cache이므로 meta는 최소화)
  // 모바일 브라우저를 위한 최소한의 캐시 방지만 추가
  if (!content.includes('http-equiv="Cache-Control"')) {
    content = content.replace(
      /<meta\s+name="viewport"[^>]*>/i,
      (match) => {
        // HTML만 재검증하도록 설정 (CSS/JS는 버전 쿼리 파라미터로 관리)
        // 카카오톡 인앱 브라우저 및 모바일 브라우저 호환성 메타 태그 추가
        return match + '\n  <meta http-equiv="Cache-Control" content="no-cache, must-revalidate" />\n  <meta name="mobile-web-app-capable" content="yes" />\n  <meta name="format-detection" content="telephone=no" />';
      }
    );
  }
  
  // partials는 버전 쿼리 파라미터 추가 (캐시는 유지하되 변경 시 무효화)
  content = content.replace(
    /fetch\(['"]([^'"]*\/assets\/partials\/([^'"]*\.html))['"]\)/g,
    (match, fullUrl, filename) => {
      const separator = fullUrl.includes('?') ? '&' : '?';
      return `fetch('${fullUrl}${separator}v=${version}', { cache: 'default' })`;
    }
  );
  
  // CSS 파일 경로에 버전 추가 (preload 제외)
  content = content.replace(
    /href=["']([^"']*\/assets\/css\/[^"']*\.css)["']/g,
    (match, url) => {
      // 이미 버전이 있으면 스킵
      if (url.includes('v=')) return match;
      const separator = url.includes('?') ? '&' : '?';
      return match.replace(url, `${url}${separator}v=${version}`);
    }
  );
  
  // JS 파일 경로에 버전 추가
  content = content.replace(
    /src=["']([^"']*\/assets\/js\/[^"']*\.js)["']/g,
    (match, url) => {
      // 이미 버전이 있으면 스킵
      if (url.includes('v=')) return match;
      const separator = url.includes('?') ? '&' : '?';
      return match.replace(url, `${url}${separator}v=${version}`);
    }
  );
  
  // preload 링크에도 버전 추가 (CSS/JS)
  content = content.replace(
    /href=["']([^"']*\/assets\/(?:css|js)\/[^"']*)(?:[?&]v=[^"']*)?["']/g,
    (match, url) => {
      // 이미 버전이 있으면 스킵
      if (url.includes('v=')) return match;
      const separator = url.includes('?') ? '&' : '?';
      return match.replace(url, `${url}${separator}v=${version}`);
    }
  );
  
  // 버전 체크 스크립트는 제거됨 (serviceAccountKey.json 의존성 제거)
  // 버전 쿼리 파라미터만 유지하여 캐시 무효화
  
  return content;
}

