function getCurrentDateYmd() {
  const now = new Date();
  const year = now.getFullYear();
  const month = String(now.getMonth() + 1).padStart(2, '0');
  const day = String(now.getDate()).padStart(2, '0');
  return `${year}-${month}-${day}`;
}

function generateSitemapXml() {
  const baseUrl = 'https://gritedu.kr';
  const lastmod = getCurrentDateYmd();
  const pages = [
    { path: '/', priority: '1.0' },
    { path: '/courses.html', priority: '0.9' },
    { path: '/instructors.html', priority: '0.9' },
    { path: '/contact.html', priority: '0.9' },
    { path: '/story.html', priority: '0.8' },
    { path: '/schedule.html', priority: '0.5' },
  ];

  const urlEntries = pages.map((page) => {
    const loc = `${baseUrl}${page.path}`;
    return [
      '  <url>',
      `    <loc>${loc}</loc>`,
      `    <lastmod>${lastmod}</lastmod>`,
      `    <priority>${page.priority}</priority>`,
      '  </url>'
    ].join('\n');
  });

  return [
    '<?xml version="1.0" encoding="UTF-8"?>',
    '<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">',
    ...urlEntries,
    '</urlset>',
    ''
  ].join('\n');
}

