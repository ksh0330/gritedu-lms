// dist 폴더 초기화
function toPosixPath(filePath) {
  return filePath.split(path.sep).join('/');
}

function walkFiles(dirPath, shouldSkipDir = () => false) {
  const files = [];
  if (!fs.existsSync(dirPath)) return files;

  const entries = fs.readdirSync(dirPath, { withFileTypes: true });
  for (const entry of entries) {
    const fullPath = path.join(dirPath, entry.name);
    if (entry.isDirectory()) {
      if (shouldSkipDir(fullPath, entry.name)) continue;
      files.push(...walkFiles(fullPath, shouldSkipDir));
    } else if (entry.isFile()) {
      files.push(fullPath);
    }
  }
  return files;
}

function getAssetRelativePath(filePath) {
  return toPosixPath(path.relative(ASSETS_DIR, filePath));
}

function getSourceReferenceFiles() {
  const sourceRoots = [
    ROOT_DIR,
    path.join(ROOT_DIR, 'members'),
    path.join(ASSETS_DIR, 'partials'),
    path.join(ASSETS_DIR, 'css'),
    path.join(ASSETS_DIR, 'js')
  ];
  const seen = new Set();
  const files = [];
  const allowedExts = new Set(['.html', '.css', '.js']);

  for (const sourceRoot of sourceRoots) {
    const discovered = walkFiles(sourceRoot, (fullPath, name) => {
      const rel = toPosixPath(path.relative(ROOT_DIR, fullPath));
      return (
        name === '.git' ||
        name === 'node_modules' ||
        name === 'dist' ||
        name === 'functions' ||
        name === 'scripts' ||
        rel.startsWith('scripts/')
      );
    });

    for (const filePath of discovered) {
      const ext = path.extname(filePath).toLowerCase();
      if (!allowedExts.has(ext)) continue;
      const rel = toPosixPath(path.relative(ROOT_DIR, filePath));
      if (seen.has(rel)) continue;
      seen.add(rel);
      files.push(filePath);
    }
  }

  return files;
}

function collectSourceImageReferences() {
  const refs = new Map();
  const assetImageRefPattern = /((?:https?:\/\/[^"'`\s)<>]+)?\/assets\/[^"'`\s)<>]+\.(?:png|jpe?g|webp|svg))(?:[?#][^"'`\s)<>]*)?/gi;

  for (const filePath of getSourceReferenceFiles()) {
    const relFile = toPosixPath(path.relative(ROOT_DIR, filePath));
    let content = '';
    try {
      content = fs.readFileSync(filePath, 'utf8');
    } catch (error) {
      console.warn(`  ⚠️  Image reference scan skipped ${relFile}: ${error.message}`);
      continue;
    }

    const lines = content.split(/\r?\n/);
    lines.forEach((line, index) => {
      const trimmed = line.trim();
      if (trimmed.startsWith('//') || trimmed.startsWith('/*') || trimmed.startsWith('*')) return;
      if (/\bplaceholder\s*=|placeholder:/i.test(line)) return;

      for (const match of line.matchAll(assetImageRefPattern)) {
        let url = match[1].split(/[?#]/)[0];
        if (/^https?:\/\//i.test(url)) {
          try {
            url = new URL(url).pathname;
          } catch (_) {
            continue;
          }
        }
        const assetRel = url.replace(/^\/assets\//, '');
        if (!assetRel || assetRel.includes('${') || assetRel.includes('*')) continue;

        const key = `assets/${assetRel}`;
        if (!refs.has(key)) {
          refs.set(key, {
            path: key,
            references: []
          });
        }
        refs.get(key).references.push(`${relFile}:${index + 1}`);
      }
    });
  }

  return [...refs.values()].sort((a, b) => a.path.localeCompare(b.path, 'en'));
}

function collectDistAssetReferences() {
  const refs = new Map();
  const distAssetRefPattern = /\/dist\/assets\/[^"'`\s)<>]+/gi;

  for (const filePath of getSourceReferenceFiles()) {
    const relFile = toPosixPath(path.relative(ROOT_DIR, filePath));
    let content = '';
    try {
      content = fs.readFileSync(filePath, 'utf8');
    } catch (error) {
      console.warn(`  ⚠️  dist asset reference scan skipped ${relFile}: ${error.message}`);
      continue;
    }

    const lines = content.split(/\r?\n/);
    lines.forEach((line, index) => {
      const trimmed = line.trim();
      if (trimmed.startsWith('//') || trimmed.startsWith('/*') || trimmed.startsWith('*')) return;

      for (const match of line.matchAll(distAssetRefPattern)) {
        const pathValue = match[0];
        if (!refs.has(pathValue)) {
          refs.set(pathValue, {
            path: pathValue,
            references: []
          });
        }
        refs.get(pathValue).references.push(`${relFile}:${index + 1}`);
      }
    });
  }

  return [...refs.values()].sort((a, b) => a.path.localeCompare(b.path, 'en'));
}

function getOriginalPreserveReason(relAssetPath) {
  const normalized = toPosixPath(relAssetPath);
  const ext = path.extname(normalized).toLowerCase();
  if (!ORIGINAL_RASTER_EXTENSIONS.has(ext)) return '';
  if (PRESERVED_ROOT_ORIGINALS.has(normalized)) {
    return 'allowlisted-root-logo-favicon-or-theme-asset';
  }
  if (normalized.startsWith('icons/')) {
    return 'allowlisted-pwa-install-icon';
  }
  if (normalized.startsWith('instructors/profile/')) {
    return 'allowlisted-instructor-profile-photo';
  }
  return '';
}

function isWebpPreferredAssetPath(relAssetPath) {
  const normalized = toPosixPath(relAssetPath);
  return WEBP_PREFERRED_ASSET_DIRS.some((dirPath) => (
    normalized === dirPath || normalized.startsWith(`${dirPath}/`)
  ));
}

function getWebpRelPath(relAssetPath) {
  return toPosixPath(relAssetPath).replace(/\.(png|jpg|jpeg)$/i, '.webp');
}

function findSourceOriginalCounterpartForWebp(relAssetPath) {
  if (path.extname(relAssetPath).toLowerCase() !== '.webp') return '';
  const withoutExt = relAssetPath.replace(/\.webp$/i, '');
  for (const ext of ORIGINAL_RASTER_EXTENSIONS) {
    const candidateRel = `${withoutExt}${ext}`;
    const candidatePath = path.join(ASSETS_DIR, ...candidateRel.split('/'));
    if (fs.existsSync(candidatePath)) {
      return `assets/${candidateRel}`;
    }
  }
  return '';
}

function validateImageAssetPolicy(report) {
  console.log('🖼️  이미지 에셋 정책 검증 중...');
  // R2 public HTTPS URLs (public/popup/, public/instructors/...) are not scanned here.
  // collectSourceImageReferences() only matches paths containing /assets/.

  const distRefs = collectDistAssetReferences();
  for (const ref of distRefs) {
    report.imageValidation.distAssetReferences.push(ref);
    console.warn(`  ⚠️  dist 경로 참조 금지: ${ref.path} (${ref.references.join(', ')})`);
  }

  for (const relAsset of REQUIRED_SOURCE_ORIGINALS) {
    const fullPath = path.join(ASSETS_DIR, relAsset);
    if (!fs.existsSync(fullPath)) {
      const item = {
        path: `assets/${relAsset}`,
        message: `${relAsset} is required as a source original by the local image asset policy.`
      };
      report.imageValidation.requiredMissing.push(item);
      console.error(`  ❌ ${item.path} 누락: ${item.message}`);
    }
  }

  const refs = collectSourceImageReferences();
  for (const ref of refs) {
    const relAssetPath = ref.path.replace(/^assets\//, '');
    const fullPath = path.join(ASSETS_DIR, ...relAssetPath.split('/'));
    if (!fs.existsSync(fullPath)) {
      const generatedFrom = findSourceOriginalCounterpartForWebp(relAssetPath);
      if (generatedFrom) {
        continue;
      }
      report.imageValidation.missingReferences.push(ref);
      console.warn(`  ⚠️  누락된 이미지 참조: ${ref.path} (${ref.references.join(', ')})`);
    }
  }

  const assetFiles = walkFiles(ASSETS_DIR, (fullPath, name) => name === 'css' || name === 'js');
  const imageFiles = assetFiles.filter((filePath) => IMAGE_EXTENSIONS.has(path.extname(filePath).toLowerCase()));

  for (const filePath of imageFiles) {
    const rel = getAssetRelativePath(filePath);
    if (RUNTIME_WEBP_SOURCE_ORIGINALS.has(rel)) {
      continue;
    }
    if (LEGACY_OPTIONAL_ROOT_ASSETS.has(rel)) {
      continue;
    }
    const stats = fs.statSync(filePath);
    if (stats.size > LARGE_SOURCE_IMAGE_BYTES) {
      report.imageValidation.largeSourceImages.push({
        path: `assets/${rel}`,
        sizeBytes: stats.size,
        sizeMB: Number((stats.size / 1024 / 1024).toFixed(2))
      });
    }
  }

  const assetDirs = walkFiles(ASSETS_DIR)
    .map((filePath) => path.dirname(getAssetRelativePath(filePath)))
    .filter((dirPath, index, all) => dirPath && all.indexOf(dirPath) === index)
    .sort((a, b) => a.localeCompare(b, 'en'));

  report.imageValidation.archiveLikeDirectories = assetDirs
    .filter((dirPath) => ARCHIVE_ASSET_DIR_PATTERN.test(dirPath))
    .map((dirPath) => `assets/${dirPath}`);

  const byStem = new Map();
  for (const filePath of imageFiles) {
    const ext = path.extname(filePath).toLowerCase().slice(1);
    const dir = path.dirname(getAssetRelativePath(filePath));
    const stem = path.basename(filePath, path.extname(filePath));
    const key = dir === '.' ? stem : `${dir}/${stem}`;
    if (!byStem.has(key)) {
      byStem.set(key, { path: `assets/${key}`, extensions: new Set() });
    }
    byStem.get(key).extensions.add(ext);
  }

  report.imageValidation.duplicatePairs = [...byStem.values()]
    .filter((item) => item.extensions.size > 1)
    .filter((item) => !LEGACY_OPTIONAL_ROOT_STEMS.has(item.path.replace(/^assets\//, '')))
    .map((item) => ({
      path: item.path,
      extensions: [...item.extensions].sort()
    }))
    .sort((a, b) => a.path.localeCompare(b.path, 'en'));

  report.imageValidation.missingWebpCounterparts = imageFiles
    .map((filePath) => getAssetRelativePath(filePath))
    .filter((rel) => ORIGINAL_RASTER_EXTENSIONS.has(path.extname(rel).toLowerCase()))
    .filter((rel) => isWebpPreferredAssetPath(rel))
    .filter((rel) => !fs.existsSync(path.join(ASSETS_DIR, ...getWebpRelPath(rel).split('/'))))
    .map((rel) => ({
      source: `assets/${rel}`,
      expected: `assets/${getWebpRelPath(rel)}`
    }))
    .sort((a, b) => a.source.localeCompare(b.source, 'en'));

  if (report.imageValidation.largeSourceImages.length > 0) {
    console.warn(`  ⚠️  1MB 초과 원본 이미지 ${report.imageValidation.largeSourceImages.length}개`);
    report.imageValidation.largeSourceImages.slice(0, 8).forEach((item) => {
      console.warn(`     - ${item.path}: ${item.sizeMB}MB`);
    });
  }

  if (report.imageValidation.archiveLikeDirectories.length > 0) {
    console.warn(`  ⚠️  assets 내부 archive/raw/test 계열 폴더 ${report.imageValidation.archiveLikeDirectories.length}개`);
    report.imageValidation.archiveLikeDirectories.forEach((dirPath) => console.warn(`     - ${dirPath}`));
  }

  if (report.imageValidation.duplicatePairs.length > 0) {
    console.warn(`  ⚠️  PNG/JPG/WebP 중복 후보 ${report.imageValidation.duplicatePairs.length}개`);
  }
  if (report.imageValidation.missingWebpCounterparts.length > 0) {
    console.warn(`  ⚠️  WebP 우선 폴더의 소스 WebP 누락 ${report.imageValidation.missingWebpCounterparts.length}개 (prebuild에서 dist WebP 생성 가능)`);
  }

  const errorCount = (
    report.imageValidation.requiredMissing.length +
    report.imageValidation.missingReferences.length +
    report.imageValidation.distAssetReferences.length
  );
  if (errorCount > 0) {
    throw new Error(`Image asset validation failed: ${errorCount} missing required/reference asset(s).`);
  }

  console.log('✅ 이미지 에셋 정책 검증 완료');
}

function distPathAccessible(targetPath) {
  try {
    fs.accessSync(targetPath, fs.constants.F_OK);
    return true;
  } catch {
    return false;
  }
}

function forceRemovePath(targetPath) {
  if (!targetPath) return true;

  if (process.platform === 'win32') {
    const winPath = targetPath.replace(/\//g, '\\');
    try {
      execSync(`cmd /c attrib -R -S -H "${winPath}" /S /D`, { stdio: 'ignore' });
    } catch (_) {}
    try {
      execSync(`cmd /c del /f /q "${winPath}"`, { stdio: 'ignore' });
    } catch (_) {}
    try {
      execSync(`cmd /c rmdir /s /q "${winPath}"`, { stdio: 'ignore' });
    } catch (_) {}
  }

  if (!distPathAccessible(targetPath)) return true;

  try {
    fs.rmSync(targetPath, { recursive: true, force: true, maxRetries: 5, retryDelay: 200 });
  } catch (_) {}

  return !distPathAccessible(targetPath);
}

function tryWriteViaTemp(tempPath, destPath, content) {
  try {
    fs.mkdirSync(path.dirname(tempPath), { recursive: true });
    fs.writeFileSync(tempPath, content, 'utf8');
    forceRemovePath(destPath);
    fs.renameSync(tempPath, destPath);
    return true;
  } catch (_) {
    try {
      forceRemovePath(destPath);
      fs.copyFileSync(tempPath, destPath);
      fs.unlinkSync(tempPath);
      return true;
    } catch (_) {
      try {
        fs.unlinkSync(tempPath);
      } catch (_) {}
      return false;
    }
  }
}

function writeDistFileSafely(destPath, content) {
  fs.mkdirSync(path.dirname(destPath), { recursive: true });
  const tempCandidates = [
    path.join(path.dirname(destPath), `.${path.basename(destPath)}.${process.pid}.tmp`),
    path.join(DIST_DIR, `.${path.basename(destPath)}.${process.pid}.tmp`)
  ];

  for (const tempPath of tempCandidates) {
    for (let attempt = 0; attempt < 5; attempt += 1) {
      if (tryWriteViaTemp(tempPath, destPath, content)) {
        return true;
      }
      const start = Date.now();
      while (Date.now() - start < 200) {
        // brief wait before retry
      }
    }
  }

  return false;
}

function writeScheduleImagesCleanUrlContent(content) {
  const cleanDest = path.join(DIST_DIR, 'schedule-images', 'index.html');
  if (writeDistFileSafely(cleanDest, content)) {
    console.log('✅ /schedule-images clean URL 생성 (dist/schedule-images/index.html)');
    return true;
  }

  console.log('ℹ️  dist/schedule-images/index.html 미생성 — Cloudflare pretty URL 또는 firebase.json rewrite 사용');
  return false;
}

function initDistDir() {
  console.log('📁 빌드 디렉토리 초기화 중...');
  forceRemovePath(path.join(DIST_DIR, 'schedule-images', 'index.html'));
  forceRemovePath(path.join(DIST_DIR, 'schedule-images'));
  if (fs.existsSync(DIST_DIR)) {
    try {
      // Windows에서 파일이 사용 중일 수 있으므로 재시도 로직 추가
      let retries = 3;
      while (retries > 0) {
        try {
          fs.rmSync(DIST_DIR, { recursive: true, force: true, maxRetries: 3, retryDelay: 100 });
          break;
        } catch (err) {
          retries--;
          if (retries === 0) {
            // 삭제 실패 시 경고만 출력하고 계속 진행 (기존 파일 덮어쓰기)
            console.warn('⚠️  dist 폴더 삭제 실패. 기존 파일을 덮어쓰기 모드로 진행합니다.');
            console.warn('   원인:', err.message);
            break;
          }
          console.log(`   ⚠️  삭제 재시도 중... (${3 - retries}/3)`);
          // 잠시 대기 후 재시도 (동기 방식)
          const start = Date.now();
          while (Date.now() - start < 500) {
            // 500ms 대기
          }
        }
      }
    } catch (err) {
      // 최종 실패 시에도 경고만 출력하고 계속 진행
      console.warn('⚠️  dist 폴더 삭제 실패. 기존 파일을 덮어쓰기 모드로 진행합니다.');
      console.warn('   원인:', err.message);
    }
  }
  // 폴더가 없으면 생성, 있으면 그대로 사용
  if (!fs.existsSync(DIST_DIR)) {
    fs.mkdirSync(DIST_DIR, { recursive: true });
  }
  console.log('✅ 빌드 디렉토리 준비 완료');
}

const LOCAL_CSS_IMPORT_PATTERN = /@import\s+(?:url\s*\(\s*)?['"]?([^'")]+)['"]?\s*\)?\s*;/gi;

function isExternalCssImport(href) {
  const normalized = String(href || '').trim();
  return /^https?:\/\//i.test(normalized) || normalized.startsWith('//');
}

function resolveCssImportPath(importHref, fromDir) {
  const href = String(importHref || '').trim().split(/[?#]/)[0];
  if (!href || isExternalCssImport(href)) {
    return null;
  }
  if (href.startsWith('/assets/css/')) {
    return path.join(ASSETS_DIR, 'css', href.slice('/assets/css/'.length));
  }
  if (href.startsWith('/')) {
    return path.join(ROOT_DIR, href.replace(/^\//, ''));
  }
  return path.resolve(fromDir, href);
}

function findLocalCssImports(content) {
  const imports = [];
  if (typeof content !== 'string' || !content.includes('@import')) {
    return imports;
  }

  for (const match of content.matchAll(LOCAL_CSS_IMPORT_PATTERN)) {
    const href = String(match[1] || '').trim();
    if (!href || isExternalCssImport(href)) {
      continue;
    }
    imports.push({ full: match[0], href });
  }

  return imports;
}

function inlineLocalCssImports(content, baseDir, seen = new Set()) {
  return content.replace(LOCAL_CSS_IMPORT_PATTERN, (fullMatch, importHref) => {
    const targetPath = resolveCssImportPath(importHref, baseDir);
    if (!targetPath) {
      return fullMatch;
    }

    const normalized = path.normalize(targetPath);
    if (seen.has(normalized)) {
      return '';
    }
    if (!fs.existsSync(normalized)) {
      throw new Error(`CSS import target not found: ${importHref} (resolved to ${targetPath})`);
    }

    seen.add(normalized);
    const imported = fs.readFileSync(normalized, 'utf8');
    const inlined = inlineLocalCssImports(imported, path.dirname(normalized), seen);
    return `${inlined}\n`;
  });
}

